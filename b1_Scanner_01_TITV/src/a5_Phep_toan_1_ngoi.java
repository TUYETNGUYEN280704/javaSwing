public class a5_Phep_toan_1_ngoi {
	public static void main(String[] args) {
		int a = 9;
		boolean b = false;
		System.out.println("-a ->  " + (-a));
		System.out.println("+a ->  " + (+a));
		System.out.println("!b ->  " + (!b));

		System.out.println("---------------");
		System.out.println("a = " + a); //9
		System.out.println("++a = " + (++a));//10
		System.out.println("a++ = " + (a++));//10
		
		System.out.println("a = " + a);//11
		
		System.out.println("--a = " + --a); //10
		System.out.println("a-- = " + a--);//10
		System.out.println("a = " + a);//9

	}
}
